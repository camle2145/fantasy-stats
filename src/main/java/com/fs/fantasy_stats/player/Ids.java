package com.fs.fantasy_stats.player;

public class Ids {
    private String gsisId;
    private String pfrId;
    private String mflId;
    private String sleeperId;
    private String fantasyDataId;
    private String rotowireId;
    private String sportradarId;
    private String rotoworldId;
    private String yahooId;
    private String espnId;
    private String statsId;

    public Ids() {
    }

    public Ids(String gsisId, String pfrId, String mflId, String sleeperId,
               String fantasyDataId, String rotowireId, String sportradarId,
               String rotoworldId, String yahooId, String espnId, String statsId) {
        this.gsisId = gsisId;
        this.pfrId = pfrId;
        this.mflId = mflId;
        this.sleeperId = sleeperId;
        this.fantasyDataId = fantasyDataId;
        this.rotowireId = rotowireId;
        this.sportradarId = sportradarId;
        this.rotoworldId = rotoworldId;
        this.yahooId = yahooId;
        this.espnId = espnId;
        this.statsId = statsId;
    }

    public String getGsisId() {
        return gsisId;
    }

    public void setGsisId(String gsisId) {
        this.gsisId = gsisId;
    }

    public String getPfrId() {
        return pfrId;
    }

    public void setPfrId(String pfrId) {
        this.pfrId = pfrId;
    }

    public String getMflId() {
        return mflId;
    }

    public void setMflId(String mflId) {
        this.mflId = mflId;
    }

    public String getSleeperId() {
        return sleeperId;
    }

    public void setSleeperId(String sleeperId) {
        this.sleeperId = sleeperId;
    }

    public String getFantasyDataId() {
        return fantasyDataId;
    }

    public void setFantasyDataId(String fantasyDataId) {
        this.fantasyDataId = fantasyDataId;
    }

    public String getRotowireId() {
        return rotowireId;
    }

    public void setRotowireId(String rotowireId) {
        this.rotowireId = rotowireId;
    }

    public String getSportradarId() {
        return sportradarId;
    }

    public void setSportradarId(String sportradarId) {
        this.sportradarId = sportradarId;
    }

    public String getRotoworldId() {
        return rotoworldId;
    }

    public void setRotoworldId(String rotoworldId) {
        this.rotoworldId = rotoworldId;
    }

    public String getYahooId() {
        return yahooId;
    }

    public void setYahooId(String yahooId) {
        this.yahooId = yahooId;
    }

    public String getEspnId() {
        return espnId;
    }

    public void setEspnId(String espnId) {
        this.espnId = espnId;
    }

    public String getStatsId() {
        return statsId;
    }

    public void setStatsId(String statsId) {
        this.statsId = statsId;
    }
}
